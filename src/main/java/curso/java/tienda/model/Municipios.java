package main.java.curso.java.tienda.model;

public class Municipios {
	int id;
	String nm;

	public Municipios(int id, String nm) {
		this.id = id;
		this.nm = nm;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNm() {
		return nm;
	}

	public void setNm(String nm) {
		this.nm = nm;
	}

}
